#include<stdio.h>

int main()
{
    int i=1;
    printf("the value of i++ is %d\n", ++i); // increment by 1
    printf("the value of ++i is %d\n", --i); //decrement by 1
    printf("teh value of i is %d\n", i+=10); //increment by 10
    printf("teh value of i is %d\n", i*=30); //multiply by 30 
    printf("teh value of i is %d\n", i/=10); //divide by 10
return 0;
}